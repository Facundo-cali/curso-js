// Sentencia Switch

let nota = 2;

switch (nota) {
    case 5:
        console.log("Enhorabuena, has obtenido un sobresaliente");
        break;
    case 4:
        console.log("Buen trabajo, pero podria ser mejor");
        break;
    case 3:
        console.log("Has obtenido un suficiente");
        break;
    case 2:
        console.log("Desaprobaste");
        break;
    case 1:
        console.log("Desaprobaste");
        break;
    default:
        console.log("Ingresa nota");
        break;
}